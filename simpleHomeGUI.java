    private void btnBillCalculationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBillCalculationActionPerformed
        // TODO add your handling code here:                                               
        //Confirm recent logs on the combo box
        //Retrieves data using DefaultTableModel
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblFurnitureLogs.getModel();
        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "No logs available for calculation.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String[] logOptions = new String[model.getRowCount()];
        for (int i = 0; i < model.getRowCount(); i++) {
            logOptions[i] = "Log " + model.getValueAt(i, 0); // Gets Log no.
        }
        
        
        String selectedLog = (String) JOptionPane.showInputDialog(
            this,
            "Select the log for calculation:",
            "Confirm Log",
            JOptionPane.QUESTION_MESSAGE,
            null,
            logOptions,
            logOptions[0]
        );

        if (selectedLog == null) return; //Canceled

        // Retrieve the selected log's details
        int selectedLogIndex = Integer.parseInt(selectedLog.split(" ")[1]) - 1;
        String logNo = model.getValueAt(selectedLogIndex, 0).toString();
        String devicesOn = model.getValueAt(selectedLogIndex, 1).toString();
        String mostConsumption = model.getValueAt(selectedLogIndex, 2).toString();
        String allConsumption = model.getValueAt(selectedLogIndex, 3).toString();

        // Display recent calculations to confirm
        String logDetails = "Log no.: " + logNo + "\n"
                          + "Devices On: " + devicesOn + "\n"
                          + "Most Consumption: " + mostConsumption + "\n"
                          + "All Consumption: " + allConsumption;

        int confirm = JOptionPane.showConfirmDialog(
            this,
            logDetails + "\n\nDo you want to proceed with this calculation?",
            "Confirm Calculation",
            JOptionPane.YES_NO_OPTION
        );

        if (confirm != JOptionPane.YES_OPTION) return; // User canceled

        // Ask for duration via combo box
        String[] durationOptions = {"24 Hours", "1 Week", "1 Month", "1 Year"};
        String selectedDuration = (String) JOptionPane.showInputDialog(
            this,
            "Select the duration for the power cost calculation:",
            "Select Duration",
            JOptionPane.QUESTION_MESSAGE,
            null,
            durationOptions,
            durationOptions[0]
        );

        if (selectedDuration == null) return; // User canceled

        // Convert total consumption from "watts" to kWh
        int totalConsumptionWatts = Integer.parseInt(allConsumption.split(" ")[0]); // Parse watt value
        double totalConsumptionKWh = totalConsumptionWatts / 1000.0;

        // Calculate duration multiplier
        double multiplier;
        switch (selectedDuration) {
            case "24 Hours": multiplier = 1; break;
            case "1 Week": multiplier = 7; break;
            case "1 Month": multiplier = 30; break;
            case "1 Year": multiplier = 365; break;
            default: multiplier = 1;
        }

        // Calculate total kWh for the selected duration
        double totalKWh = totalConsumptionKWh * multiplier;

        // Use the overall rate of Php 12.0545 per kWh
        double costPerKWh = 12.0545;
        double totalCost = totalKWh * costPerKWh;

        // Display the total cost
        String costMessage = String.format(
            "Total Consumption: %.2f kWh\nDuration: %s\nCost per kWh: Php %.4f\n\nTotal Cost: Php %.2f",
            totalKWh, selectedDuration, costPerKWh, totalCost
        );

        JOptionPane.showMessageDialog(this, costMessage, "Electricity Bill", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnBillCalculationActionPerformed

