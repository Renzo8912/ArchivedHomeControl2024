    private void btnComputeCostActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComputeCostActionPerformed
        // TODO add your handling code here:
        // listeners for each room
        ComfortRoomCalculator crCalculator = new ComfortRoomCalculator(
                tbtnCR_MainBulb.isSelected(),
                tbtnCR_CRExhaust.isSelected(),
                tbtnCR_ShowerBulb.isSelected()
        );

        KitchenCalculator kitchenCalculator = new KitchenCalculator(
                tbtnK_MainBulb.isSelected(),
                tbtnK_RearBulb.isSelected(),
                tbtnK_SinkBulb.isSelected(),
                tbtnK_StoveBulb.isSelected(),
                tbtnK_DeskBulb.isSelected(),
                tbtnK_AirVent.isSelected(),
                tbtnK_RearAirVent.isSelected(),
                tbtnK_Washer.isSelected(),
                tbtnK_Dryer.isSelected()
        );

        LivingRoomCalculator livingRoomCalculator = new LivingRoomCalculator(
                tbtnLR_AirCondition.isSelected(),
                tbtnLR_Bulb1.isSelected(),
                tbtnLR_Bulb2.isSelected(),
                tbtnLR_Chandelier.isSelected(),
                tbtnLR_Heater.isSelected(),
                tbtnLR_SaltLamp.isSelected(),
                tbtnLR_StandLamp.isSelected(),
                tbtnLR_Television.isSelected()
        );

        BedroomCalculator bedroomCalculator = new BedroomCalculator(
                tbtnB_AirConditioner.isSelected(),
                tbtnB_ElectricFan.isSelected(),
                tbtnB_Heater.isSelected(),
                tbtnB_LeftLamp.isSelected(),
                tbtnB_MainBulb.isSelected(),
                tbtnB_RightLamp.isSelected()
        );

        //Consumption for each room (checks the wattage of each room)
        crCalculator.calculateConsumption();
        kitchenCalculator.calculateConsumption();
        livingRoomCalculator.calculateConsumption();
        bedroomCalculator.calculateConsumption();

        //Count devices that are online
        int totalDevicesOn = crCalculator.getDevicesOn() 
                           + kitchenCalculator.getDevicesOn() 
                           + livingRoomCalculator.getDevicesOn() 
                           + bedroomCalculator.getDevicesOn();

        if (totalDevicesOn == 0) {
            JOptionPane.showMessageDialog(this, 
                    "No devices are On.", 
                    "BABALA!", 
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Maximum consumption
        //calculates the maximum consumption of all rooms
        int maxConsumption = Math.max(
            crCalculator.getTotalConsumption(),
            Math.max(
                kitchenCalculator.getTotalConsumption(),
                Math.max(
                    livingRoomCalculator.getTotalConsumption(),
                    bedroomCalculator.getTotalConsumption()
                )
            )
        );

        // Room with the most consumption
        String mostConsumptionRoom;
        if (maxConsumption == crCalculator.getTotalConsumption()) {
            mostConsumptionRoom = "Comfort Room";
        } else if (maxConsumption == kitchenCalculator.getTotalConsumption()) {
            mostConsumptionRoom = "Kitchen";
        } else if (maxConsumption == livingRoomCalculator.getTotalConsumption()) {
            mostConsumptionRoom = "Living Room";
        } else if (maxConsumption == bedroomCalculator.getTotalConsumption()) {
            mostConsumptionRoom = "Bedroom";
        } else {
            mostConsumptionRoom = "Tie";
        }

        //log to tblFurnitureLogs
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblFurnitureLogs.getModel();
        model.addRow(new Object[]{
            model.getRowCount() + 1,
            totalDevicesOn,
            mostConsumptionRoom,
            (crCalculator.getTotalConsumption() + kitchenCalculator.getTotalConsumption() +
                    livingRoomCalculator.getTotalConsumption() + bedroomCalculator.getTotalConsumption()) + " watts"
        });
        
        //makes sure it is updated
        tblFurnitureLogs.repaint();
        
    }//GEN-LAST:event_btnComputeCostActionPerformed

